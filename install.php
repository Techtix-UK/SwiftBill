</head>
<body class="bg-slate-100 flex items-center justify-center min-h-screen p-4 font-sans text-slate-800">

    <div class="max-w-md w-full bg-white rounded-xl shadow-lg border border-slate-200 overflow-hidden">
        <div class="bg-slate-900 p-6 text-center">
            <h1 class="text-2xl font-bold text-white tracking-tight">⚡ SwiftBill Setup</h1>
            <p class="text-slate-400 text-sm mt-1">Connect your database to generate tables</p>
        </div>

        <div class="p-6">
            <?php if ($error): ?>
                <div class="bg-red-50 text-red-700 border border-red-200 p-3 rounded-lg text-sm mb-5 font-medium">
                    <?= htmlspecialchars($error) ?>
                </div>
            <?php endif; ?>

            <?php if ($success): ?>
                <div class="bg-emerald-50 text-emerald-700 border border-emerald-200 p-4 rounded-lg text-center mb-5">
                    <p class="font-bold text-lg mb-1">Success!</p>
                    <p class="text-sm mb-4"><?= htmlspecialchars($success) ?></p>
                    <a href="index.php" class="inline-block bg-slate-900 hover:bg-slate-800 text-white font-medium py-2 px-6 rounded transition">
                        Go to Dashboard
                    </a>
                </div>
            <?php else: ?>
                <form method="POST" action="" class="space-y-4">
                    
                    <div>
                        <label class="block text-sm font-semibold mb-1 text-slate-700">Database Driver</label>
                        <select name="driver" class="w-full border border-slate-300 rounded-lg p-2.5 bg-slate-50 focus:ring-2 focus:ring-indigo-500 focus:outline-none">
                            <option value="mysql">MySQL / MariaDB</option>
                            <option value="pgsql">PostgreSQL</option>
                        </select>
                    </div>

                    <div>
                        <label class="block text-sm font-semibold mb-1 text-slate-700">Host (usually localhost)</label>
                        <input type="text" name="host" value="localhost" required class="w-full border border-slate-300 rounded-lg p-2.5 bg-slate-50 focus:ring-2 focus:ring-indigo-500 focus:outline-none">
                    </div>

                    <div>
                        <label class="block text-sm font-semibold mb-1 text-slate-700">Database Name</label>
                        <input type="text" name="db" placeholder="e.g., swiftbill" required class="w-full border border-slate-300 rounded-lg p-2.5 bg-slate-50 focus:ring-2 focus:ring-indigo-500 focus:outline-none">
                    </div>

                    <div class="grid grid-cols-2 gap-4">
                        <div>
                            <label class="block text-sm font-semibold mb-1 text-slate-700">Username</label>
                            <input type="text" name="user" required class="w-full border border-slate-300 rounded-lg p-2.5 bg-slate-50 focus:ring-2 focus:ring-indigo-500 focus:outline-none">
                        </div>
                        <div>
                            <label class="block text-sm font-semibold mb-1 text-slate-700">Password</label>
                            <input type="password" name="pass" class="w-full border border-slate-300 rounded-lg p-2.5 bg-slate-50 focus:ring-2 focus:ring-indigo-500 focus:outline-none">
                        </div>
                    </div>

                    <button type="submit" class="w-full bg-indigo-600 hover:bg-indigo-700 text-white font-bold py-3 px-4 rounded-lg mt-4 shadow-sm transition">
                        Install & Create Tables
                    </button>
                </form>
            <?php endif; ?>
        </div>
    </div>

</body>
