<?php
$error = '';
$success = '';

// Security: Prevent re-running the installer
if (file_exists('install.lock')) {
    die("<div style='font-family: sans-serif; padding: 2rem; text-align: center;'>
            <h2>Installation Locked</h2>
            <p>The system is already installed. To run this wizard again, delete the <b>install.lock</b> file.</p>
         </div>");
}

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $driver = $_POST['driver'] ?? 'mysql';
    $host   = $_POST['host'] ?? 'localhost';
    $db     = $_POST['db'] ?? '';
    $user   = $_POST['user'] ?? '';
    $pass   = $_POST['pass'] ?? '';

    try {
        // 1. Attempt Database Connection
        $dsn = $driver === 'mysql' 
            ? "mysql:host=$host;dbname=$db;charset=utf8mb4" 
            : "pgsql:host=$host;dbname=$db";
        
        $pdo = new PDO($dsn, $user, $pass, [
            PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION
        ]);

        // 2. Determine Primary Key Syntax based on DB Driver
        $pk_syntax = $driver === 'mysql' ? 'INT AUTO_INCREMENT PRIMARY KEY' : 'SERIAL PRIMARY KEY';

        // 3. Define Table Schema Queries
        $queries = [
            "CREATE TABLE IF NOT EXISTS clients (
                id $pk_syntax,
                name VARCHAR(255) NOT NULL,
                email VARCHAR(255) NOT NULL,
                company VARCHAR(255),
                address TEXT,
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )",
            "CREATE TABLE IF NOT EXISTS products (
                id $pk_syntax,
                name VARCHAR(255) NOT NULL,
                description TEXT,
                price DECIMAL(10, 2) NOT NULL,
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )",
            "CREATE TABLE IF NOT EXISTS invoices (
                id $pk_syntax,
                client_id INT NOT NULL,
                invoice_number VARCHAR(50) NOT NULL UNIQUE,
                status VARCHAR(20) DEFAULT 'draft',
                type VARCHAR(20) DEFAULT 'invoice',
                issue_date DATE NOT NULL,
                due_date DATE,
                total_amount DECIMAL(10, 2) DEFAULT 0.00,
                stripe_payment_intent_id VARCHAR(255),
                created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
            )",
            "CREATE TABLE IF NOT EXISTS invoice_items (
                id $pk_syntax,
                invoice_id INT NOT NULL,
                product_id INT,
                description TEXT NOT NULL,
                quantity INT NOT NULL DEFAULT 1,
                unit_price DECIMAL(10, 2) NOT NULL,
                subtotal DECIMAL(10, 2) NOT NULL
            )"
        ];

        // 4. Execute Table Creation
        foreach ($queries as $sql) {
            $pdo->exec($sql);
        }

        // 5. Generate and Write db.php
        $config_content = "<?php\n" .
            "// Auto-generated by install.php\n" .
            "\$db_driver = '$driver';\n" .
            "\$db_host   = '$host';\n" .
            "\$db_name   = '$db';\n" .
            "\$db_user   = '$user';\n" .
            "\$db_pass   = '$pass';\n\n" .
            "try {\n" .
            "    \$dsn = \$db_driver === 'mysql' \n" .
            "        ? \"mysql:host=\$db_host;dbname=\$db_name;charset=utf8mb4\" \n" .
            "        : \"pgsql:host=\$db_host;dbname=\$db_name\";\n" .
            "    \$pdo = new PDO(\$dsn, \$db_user, \$db_pass, [\n" .
            "        PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION,\n" .
            "        PDO::ATTR_DEFAULT_FETCH_MODE => PDO::FETCH_ASSOC\n" .
            "    ]);\n" .
            "} catch (PDOException \$e) {\n" .
            "    die('Database connection failed: ' . \$e->getMessage());\n" .
            "}\n";

        if (file_put_contents('db.php', $config_content) === false) {
            throw new Exception("Could not write to db.php. Check your directory permissions.");
        }

        // 6. Create lock file
        file_put_contents('install.lock', 'Locked. Delete this file to reinstall.');

        $success = "Database configured and tables created! You are ready to go.";

    } catch (PDOException $e) {
        $error = "Database Error: " . $e->getMessage();
    } catch (Exception $e) {
        $error = "System Error: " . $e->getMessage();
    }
}
